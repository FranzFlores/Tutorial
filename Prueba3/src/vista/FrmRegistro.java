    private void cargarObjeto() {
        sn.obtenerPersona().setCedula(txt_cedula.getText());
        sn.obtenerPersona().setNombre(txt_nombre.getText());
        sn.obtenerPersona().setDireccion(txt_direccion.getText());
        sn.obtenerPersona().setTelefono(txt_telefono.getText());
        sn.obtenerPersona().setCiudad(txt_ciudad.getText());
        if (rd_si.isSelected()) {
            sn.obtenerPersona().setTiene_discacidad(true);
            sn.obtenerPersona().setPorcentaje(txt_porcentaje.getText());
            sn.obtenerPersona().setTipo((TipoDiscapacidad) cbx_tipo.getSelectedItem());
        }else if (rd_no.isSelected()) {
            sn.obtenerPersona().setTiene_discacidad(false);
            sn.obtenerPersona().setPorcentaje("Ninguna");
            sn.obtenerPersona().setTipo((TipoDiscapacidad) cbx_tipo.getSelectedItem());
        }
    }
    
    private void mostrarCombo(){
        if (rd_si.isSelected()) {
            porcentaje.setVisible(true);
            tipo.setVisible(true);
            cbx_tipo.setVisible(true);
            txt_porcentaje.setVisible(true);
        } else if (rd_no.isSelected()) {
            porcentaje.setVisible(false);
            tipo.setVisible(false);
            cbx_tipo.setVisible(false);
            txt_porcentaje.setVisible(false);
        }
    }

    private void cargarCombo() {
        UtilidadesComponente.cargarcomboTipo(cbx_tipo);
        cbx_tipo.updateUI();
    }
    

