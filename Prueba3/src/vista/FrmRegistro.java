    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
       if (!UtilidadesComponente.mostrarError(txt_cedula, "Falta este dato", 'r')
            && !UtilidadesComponente.mostrarError(txt_nombre, "Falta este dato", 'r')
            && !UtilidadesComponente.mostrarError(txt_direccion, "Falta este dato", 'r')
            && !UtilidadesComponente.mostrarError(txt_telefono, "Falta este dato", 'r')) {
            cargarObjeto();
            if (sn.obtenerPersona().getId() == null) {
              if (sn.guardar()) {                   
                    UtilidadesComponente.mensajeOk("OK", "Se ha guardado correctamente");
                } else {
                    UtilidadesComponente.mensajeOk("ERROR", "No se pudo guardar correctamente");
                }
            }
        } 
        
    }//GEN-LAST:event_btn_guardarActionPerformed

